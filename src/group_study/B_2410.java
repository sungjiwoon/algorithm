package group_study;

import java.util.Scanner;

public class B_2410 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] dp = new int[n+1];
        dp[1] = 1;

        for (int i = 2; i <= n; i++) {
            if (i % 2 == 1) {
                dp[i] = dp[i-1];
            } else {
                dp[i] = dp[i-1]+dp[i/2];
            }
            dp[i] %= 1000000000;
        }

        System.out.println(dp[n]);

        /*
        1(1) -> 1
        2(2) -> 1+1, 2 => d[1] + 1
        3(2) -> 1+1+1, 1+2
        4(4) -> 1+1+1+1, 1+1+2, 2+2, 4 => d[3] + d[2]
        5(4) -> 11111, 1112, 122, 14
        6(6) -> 111111, 11112, 1122, 114, 24, 222 => d[5] + d[3] = 6
        7(6) -> 111111, 111112, 11122, 1222, 1114, 124
        8(10) -> 11111111, 1111112, 111122, 11222, 2222, 11114, 1124, 224, 44,8 => d[7] + d[4]
        10(14) -> 1*10, 1*8+2, 1*6+22, 1*6+4, 1*4+222, 1*4+24, 1*2+2222,1*2+224, 1*2+44, 1*2+8, 22222, 4222, 442, 82
        d[9] + d[5] = 10+4
        12(20) -> 1*12, 1*10+2,1*8+22,1*8+4,1*6+222,1*6+24, 1*4+2222,1*4+224, 1*4+44, 1*4+8, 1*2+22222, 1*2+4222, 1*2+442
                   222222, 22224, 2244, 444, 228, 48 -> d[11] + d[6] = 14+6

        * 규칙 :
        홀수시 : d[i-1]
        짝수시 : d[i-1]+ d[i/2] 값

         */
    }
}
